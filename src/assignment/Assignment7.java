package assignment;

public class Assignment7 {

	public static void main(String[] args) {

	/**
	 * @author Abhishek kumar
	 * Write Program to 10 to 1 using for, while and do while loop 
	 */


		System.out.println("Write program using for loop");
		for(int i = 10; i>=1; i--) {
			System.out.println(i);
		}
		
		System.out.println("Write program using while loop");
		int j =10;
		while(j>=1) {
			System.out.println(j);
			j--;
		}
		
		System.out.println("Write program using do while loop");
		int r =10;
		do {
			System.out.println(r);
			r--;
		}while(r>=1);
	}

}
